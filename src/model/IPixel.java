package model;

/**
 * An interface to represent pixels.
 */
public interface IPixel {

  /**
   * Returns the red value of the pixel.
   *
   * @return the r int.
   */
  int getR();

  /**
   * Returns the green value of the pixel.
   *
   * @return the g int.
   */
  int getG();

  /**
   * Returns the blue value of the pixel.
   *
   * @return the b int.
   */
  int getB();
}
